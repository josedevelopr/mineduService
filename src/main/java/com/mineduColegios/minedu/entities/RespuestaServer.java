package com.mineduColegios.minedu.entities;

public class RespuestaServer 
{
	private String respuestaServer;
	
	private String estadoRespuestaServer;

	public String getRespuestaServer() {
		return respuestaServer;
	}

	public void setRespuestaServer(String respuestaServer) {
		this.respuestaServer = respuestaServer;
	}

	public String getEstadoRespuestaServer() {
		return estadoRespuestaServer;
	}

	public void setEstadoRespuestaServer(String estadoRespuestaServer) {
		this.estadoRespuestaServer = estadoRespuestaServer;
	}
	
	
}
